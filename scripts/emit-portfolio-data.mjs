// Emits components/portfolio/dti-projects.ts from the DPMC master JSON.
// Usage: node scripts/emit-portfolio-data.mjs
import fs from "node:fs";
import path from "node:path";

const DPMC =
  "C:/Users/arpra/claude-api-project/Apps/Alfatrees Design & Construction Project Management Consultancy/Alfatrees DPMC (Design & Project Management Consultancy)/portfolio-projects";
const MASTER = JSON.parse(fs.readFileSync(path.join(DPMC, "projects-master.json"), "utf8"));

const STOCK = {
  Roofing: ["10-architectural-detail.jpg", "12-bridge-structure.jpg"],
  "General Construction": ["01-construction-site.jpg", "02-building-crane.jpg", "05-concrete-building.jpg", "17-construction-workers.jpg"],
  "Mechanical & Plumbing": ["13-warehouse-industrial.jpg", "03-glass-tower.jpg"],
  "HVAC & Ventilation": ["13-warehouse-industrial.jpg", "03-glass-tower.jpg"],
  "Electrical & Security": ["09-office-interior.jpg"],
  "Specialty & Civil": ["20-recreation-center.jpg", "15-school-campus.jpg"],
};

// Guard: every trade in the master data must have a STOCK pool — fail loudly, not silently.
const unknownTrades = [...new Set(MASTER.map((p) => p.trade))].filter((t) => !(t in STOCK));
if (unknownTrades.length > 0) {
  throw new Error(
    `No STOCK pool for trade(s): ${unknownTrades.join(", ")} — update STOCK in scripts/emit-portfolio-data.mjs`
  );
}

const counters = {};
const projects = MASTER.map((p) => {
  const pool = STOCK[p.trade];
  const n = counters[p.trade] ?? 0;
  counters[p.trade] = n + 1;
  const thumbnail = p.images[0] ?? `/portfolio/images/${pool[n % pool.length]}`;
  const { sources, parentProject, contractDate, ...pub } = p; // research URLs stay private
  return { ...pub, thumbnail };
});

const banner = `// AUTO-GENERATED by scripts/emit-portfolio-data.mjs — DO NOT EDIT BY HAND.
// Source of truth: DPMC portfolio-projects/projects-master.json
import type { DTIProject } from "./portfolio-types";

export const DTI_PROJECTS: DTIProject[] = `;
fs.writeFileSync(
  "components/portfolio/dti-projects.ts",
  banner + JSON.stringify(projects, null, 2) + ";\n"
);
console.log(`Emitted ${projects.length} projects → components/portfolio/dti-projects.ts`);
